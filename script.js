let currentQuestionIndex = 0;
let wrongAnswers = [];
let score = 0;
let totalAnswered = 0;
let totalQuestions = 0; // will be set after questions array definition

function updateProgress() {
    const progress = totalQuestions ? (totalAnswered / totalQuestions) * 100 : 0;
    document.getElementById('progress').style.width = `${progress}%`;
    document.getElementById('progress-text').textContent = `${totalAnswered}/${totalQuestions}`;
}

function updateScore(points) {
    score += points;
    document.getElementById('score').textContent = score;
}

function showQuestion() {
    const question = questions[currentQuestionIndex];
    document.getElementById('question').innerHTML = question.question;
    const optionsDiv = document.getElementById('options');
    optionsDiv.innerHTML = '';
    question.options.forEach(option => {
        const button = document.createElement('button');
        button.className = 'option';
        button.textContent = option;
        button.onclick = () => checkAnswer(option, question.correct);
        optionsDiv.appendChild(button);
    });
    document.getElementById('explanation').style.display = 'none';
    document.getElementById('next').style.display = 'none';
    document.getElementById('reset').style.display = 'block';
}

function checkAnswer(selected, correct) {
    const options = document.querySelectorAll('.option');
    options.forEach(option => {
        option.disabled = true;
        option.style.pointerEvents = 'none';
        if (option.textContent === selected) {
            if (selected === correct) {
                option.classList.add('correct');
                updateScore(10);
            } else {
                option.classList.add('incorrect');
                wrongAnswers.push(currentQuestionIndex);
            }
        }
        if (option.textContent === correct && selected !== correct) {
            option.classList.add('correct');
        }
    });
    const explanation = document.getElementById('explanation');
    explanation.textContent = questions[currentQuestionIndex].explanation;
    explanation.style.display = 'block';
    document.getElementById('next').style.display = 'block';
    totalAnswered++;
    updateProgress();
}

function nextQuestion() {
    if (wrongAnswers.length > 0) {
        currentQuestionIndex = wrongAnswers.shift();
    } else {
        currentQuestionIndex++;
    }
    if (currentQuestionIndex < questions.length) {
        showQuestion();
    } else {
        const scoreText = `Ваш итоговый счёт: ${score}`;
        document.getElementById('question').innerHTML = 'Викторина завершена!';
        document.getElementById('options').innerHTML = `<div class="final-score">${scoreText}</div>`;
        document.getElementById('explanation').style.display = 'none';
        document.getElementById('next').style.display = 'none';
    }
}

function resetQuiz() {
    currentQuestionIndex = 0;
    wrongAnswers = [];
    score = 0;
    totalAnswered = 0;
    updateScore(0);
    updateProgress();
    showQuestion();
}

document.getElementById('next').onclick = nextQuestion;
document.getElementById('reset').onclick = resetQuiz;

